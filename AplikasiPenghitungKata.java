    private void aktifkanListener() {
        txtInput.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) { hitungOtomatis(); }
            @Override
            public void removeUpdate(DocumentEvent e) { hitungOtomatis(); }
            @Override
            public void changedUpdate(DocumentEvent e) { hitungOtomatis(); }
        });
    }

    
    private void hitungOtomatis() {
        String teks = txtInput.getText();
        String[] kata = teks.trim().split("\\s+");
        String[] kalimat = teks.split("[.!?]+");
        String[] paragraf = teks.split("\\n+");

        int jumlahKata = teks.trim().isEmpty() ? 0 : kata.length;
        int jumlahKarakter = teks.replace(" ", "").length();
        int jumlahKalimat = teks.trim().isEmpty() ? 0 : kalimat.length;
        int jumlahParagraf = teks.trim().isEmpty() ? 0 : paragraf.length;

        lblKata.setText(String.valueOf(jumlahKata));
        lblKarakter.setText(String.valueOf(jumlahKarakter));
        lblKalimat.setText(String.valueOf(jumlahKalimat));
        lblParagraf.setText(String.valueOf(jumlahParagraf));
    }
    
    

