    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    try {
    JFileChooser chooser = new JFileChooser();
    int pilih = chooser.showSaveDialog(this);
    if (pilih == JFileChooser.APPROVE_OPTION) {
        File file = chooser.getSelectedFile();
        FileWriter fw = new FileWriter(file);
        fw.write("Teks:\n" + txtInput.getText() + "\n\n");
        fw.write("Jumlah Kata: " + lblKata.getText() + "\n");
        fw.write("Jumlah Karakter: " + lblKarakter.getText() + "\n");
        fw.write("Jumlah Kalimat: " + lblKalimat.getText() + "\n");
        fw.write("Jumlah Paragraf: " + lblParagraf.getText() + "\n");
        fw.close();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
    }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }

    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        hitungOtomatis();
        if (!realtimeAktif) {
            aktifkanListener();
            realtimeAktif = true; // ubah status agar tidak dipasang dua kali
            System.out.println("Realtime aktif!");
        }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
    String teks = txtInput.getText().toLowerCase();
    String cari = txtCari.getText().toLowerCase();

    if (cari.isEmpty()) {
        lblHasilCari.setText("Masukkan kata yang ingin dicari!");
        return;
    }

    int hitung = 0;
    for (String kata : teks.split("\\s+")) {
        if (kata.equals(cari)) hitung++;
    }

    lblHasilCari.setText("Kata \"" + cari + "\" ditemukan sebanyak: " + hitung);
    }//GEN-LAST:event_btnCariActionPerformed

